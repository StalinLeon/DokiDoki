    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        if (tfNombre.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debe de ingresar el nombre");
            tfNombre.requestFocus();
            return;
        }
        if (tfNumCedula.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debe de ingresar la cedula");
            tfNumCedula.requestFocus();
            return;
        }
        if (tfNumTelefono.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debe de ingresar el numero de teléfono");
            tfNumTelefono.requestFocus();
            return;
        }
        if (tfDireccion.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debe de ingresar el la direccion");
            tfDireccion.requestFocus();
            return;
        }

        PropietarioMascota pm = new PropietarioMascota();
        fPropietarioMascotas fpm = new fPropietarioMascotas();  
        
        pm.setNombre(tfNombre.getText());
        pm.setNumCedula(tfNumCedula.getText());
        pm.setNumTelfono(tfNumTelefono.getText());
        pm.setDireccion(tfDireccion.getText());
        
        if(accion.equals("guardar")){
            fpm.insertar(tablaListado,pm);
            JOptionPane.showMessageDialog(rootPane, "Los datos del Propietario de la Mascota ha sido registrado");
                    }
        if(accion.equals("editar")){
            pm.setIdPropietarioMasca(Integer.parseInt(tfIdPropietarioMascota.getText()));
            fpm.editar(pm); 
            JOptionPane.showMessageDialog(rootPane, "Los datos del Propietario de la Mascota ha sido editados");
            
            
        }
        mostrar("");
        inhabilitar();
    }//GEN-LAST:event_btnGuardarActionPerformed

