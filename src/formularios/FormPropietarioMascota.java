    private void tablaListadoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaListadoMouseClicked
        // TODO add your handling code here:
        btnGuardar.setText("Editar");
        habilitar();
        btnEliminar.setEnabled(true);
        accion = "editar";
        
        int fila = tablaListado.rowAtPoint(evt.getPoint());
        tfIdPropietarioMascota.setText(tablaListado.getValueAt(fila, 0).toString());
        tfNombre.setText(tablaListado.getValueAt(fila, 1).toString());
        tfNumCedula.setText(tablaListado.getValueAt(fila, 2).toString());
        tfNumTelefono.setText(tablaListado.getValueAt(fila, 3).toString());
        tfDireccion.setText(tablaListado.getValueAt(fila, 3).toString());
        
        
        
    }//GEN-LAST:event_tablaListadoMouseClicked

